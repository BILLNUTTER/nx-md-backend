export async function cmd_translate2(ctx: CommandContext) {
  const { sock, replyTo, args, prefix, command } = ctx;
  if (!args.trim()) {
    await sock.sendMessage(replyTo, {
      text: `🌍 *AI Translate*\n\nUsage: *${prefix}${command} <language>: <text>*\nExample: *${prefix}translate2 Spanish: Hello, how are you?*`,
    });
    return;
  }
  const colonIndex = args.indexOf(":");
  if (colonIndex === -1) {
    await sock.sendMessage(replyTo, {
      text: `Usage: *${prefix}translate2 <language>: <text>*\nExample: *${prefix}translate2 French: Good morning*`,
    });
    return;
  }
  const lang = args.slice(0, colonIndex).trim();
  const text = args.slice(colonIndex + 1).trim();
  await sock.sendMessage(replyTo, { text: "🌍 _Translating..._" });
  try {
    const prompt = `Translate the following text to ${lang}. Only output the translation, nothing else:\n\n"${text}"`;
    const response = await callFreeAI(prompt);
    await sock.sendMessage(replyTo, {
      text: `🌍 *Translation to ${lang}*\n\n📥 Original: ${text}\n📤 Translated: ${response}`,
    });
  } catch {
    await sock.sendMessage(replyTo, { text: `❌ Translation failed. Try again.` });
  }
}

export async function cmd_translate(ctx: CommandContext) {
  await cmd_translate2(ctx);
}

