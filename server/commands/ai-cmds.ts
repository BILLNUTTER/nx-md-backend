import https from "https";
import { CommandContext } from "./types";

function fetchText(url: string, opts: { timeout?: number } = {}): Promise<string> {
  return new Promise((resolve, reject) => {
    const req = https.get(url, { headers: { "User-Agent": "NX-MD-Bot/1.0" } }, (res) => {
      let data = "";
      res.on("data", (c: any) => { data += c; });
      res.on("end", () => resolve(data));
    });
    req.on("error", reject);
    req.setTimeout(opts.timeout || 12000, () => { req.destroy(); reject(new Error("timeout")); });
  });
}

async function callFreeAI(prompt: string): Promise<string> {
  const url = `https://text.pollinations.ai/${encodeURIComponent(prompt)}`;
  const text = await fetchText(url, { timeout: 15000 });
  return text.trim() || "Sorry, I couldn't generate a response right now.";
}

async function aiCommand(ctx: CommandContext, label: string, systemPrompt: string) {
  const { sock, replyTo, args, prefix, command } = ctx;
  if (!args.trim()) {
    await sock.sendMessage(replyTo, {
      text: `🤖 *${label}*\n\nUsage: *${prefix}${command} <prompt>*\nExample: *${prefix}${command} ${systemPrompt.slice(0, 50)}...*`,
    });
    return;
  }
  await sock.sendMessage(replyTo, { text: "🤔 _Processing..._" });
  try {
    const fullPrompt = `${systemPrompt}\n\nUser: ${args.trim()}\nAssistant:`;
    const response = await callFreeAI(fullPrompt);
    await sock.sendMessage(replyTo, {
      text: `🤖 *${label}*\n\n${response}\n\n_Powered by NX-MD AI_`,
    });
  } catch {
    await sock.sendMessage(replyTo, {
      text: `❌ AI service temporarily unavailable. Try again later.`,
    });
  }
}

